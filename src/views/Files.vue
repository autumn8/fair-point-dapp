import fairPointService from "@/fairPointService";
import { __ } from "@/utils";
import errorMessages from "@/errorMessages";

export default {
  data() {
    return {
      cards: []
    };
  },
  async created() {
    const filesResponse = await __(fairPointService.getAllFiles());
